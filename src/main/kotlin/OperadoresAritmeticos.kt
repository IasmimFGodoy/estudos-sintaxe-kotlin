class OperadoresAritmeticos {
    fun OperadoresAritmeticos(){
        /*Operadores Aritméticos
*| + Operador de Soma
*| - Operador de Subtração
*| / Operador de Divisão
*| * Operador de Multiplicação
*| % Módulo - Resto da Divisão
*| ++ -> Incremento
*| -- ->Decremento
*/
        var num1 = 50
        var num2 = 20.5
        /*Operador de Soma*/
        println(num1 + num2)
        /*Operador de Subtração*/
        println(num1 - num2)
        /*Operador de Divisão*/
        println(num1 / num2)
        /*Operador de Multiplicação*/
        println(num1 * num2)
        /*Módulo - Resto da divisão*/
        println(num1 % num2)
        /*Incremento*/
        println(++num1)
        println(++num2)
        /*Decremento*/
        println(--num1)
        println(--num2)
    }
}